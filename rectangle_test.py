def test_rectangle():
    """
    Напишите программу, которая берет длину и ширину прямоугольника
    и считает его периметр и площадь.
    """
    a = 10
    b = 20

    # Считаем периметр
    perimeter = 2 * (a + b)
    assert perimeter == 60

    # Считаем площадь
    area = a * b
    assert area == 200

    # Выводим результаты
    print(f"Периметр: {perimeter}, Площадь: {area}")
